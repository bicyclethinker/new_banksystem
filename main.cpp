#include "BankSystem.h"   // 银行业务系统类，负责账户、交易、状态管理等核心业务
#include "ConsoleUI.h"    // 控制台界面类，负责菜单显示和用户交互
#include "FileManager.h"  // 文件管理类，负责系统数据的读取和保存

#include <iostream>       // 标准输入输出，用于 cout 输出提示信息

/*
 * 程序主函数
 *
 * main 是整个银行账户模拟系统的入口。
 * 程序启动后，会先创建银行系统对象，
 * 然后尝试从文件中读取已有账户和交易数据，
 * 接着处理已经到期的定期账户，
 * 最后启动控制台菜单，等待用户操作。
 */
int main()
{
    /*
     * 创建银行业务系统对象。
     *
     * bankSystem 是整个程序的核心数据对象，
     * 里面保存账户链表、交易流水等信息，
     * 并提供开户、销户、存款、取款、转账、挂失等业务函数。
     */
    BankSystem bankSystem;

    /*
     * 从文件中读取系统数据。
     *
     * FileManager::loadAll(bankSystem)
     * 会把之前保存的账户信息和交易流水加载到 bankSystem 中。
     *
     * 如果读取失败，程序不会直接退出，
     * 而是提示用户“使用空系统启动”。
     * 这样即使第一次运行没有数据文件，系统也能正常启动。
     */
    if (!FileManager::loadAll(bankSystem)) {
        std::cout
            << "数据文件读取失败，将使用空系统启动。\n";
    }

    /*
     * 处理到期的定期账户。
     *
     * 程序启动后先检查所有定期账户是否已经到期。
     * 如果有账户到期，就进行利息计算、余额更新、
     * 账户类型转换或生成相关交易记录。
     *
     * 这一步放在菜单启动前执行，
     * 可以保证用户进入系统后看到的是最新账户状态。
     */
    bankSystem.processMaturedAccounts();

    /*
     * 创建控制台界面对象。
     *
     * ConsoleUI 通过引用绑定 bankSystem，
     * 之后用户在菜单中的所有操作，
     * 都会作用在这个同一个银行系统对象上。
     */
    ConsoleUI consoleUI(bankSystem);

    /*
     * 启动控制台主循环。
     *
     * run() 一般会不断显示主菜单，
     * 读取用户输入，
     * 并根据用户选择调用开户、存款、取款、查询等菜单函数。
     *
     * 当用户选择退出系统时，run() 结束，程序继续向下执行。
     */
    consoleUI.run();

    /*
     * 程序正常结束。
     *
     * return 0 表示主函数成功执行完毕。
     */
    return 0;
}